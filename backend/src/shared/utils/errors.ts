export class AppError extends Error {
  constructor(
    message: string,
    public statusCode: number = 500,
    public code?: string,
  ) {
    super(message);
    this.name = this.constructor.name;
  }
}

export class NotFoundError extends AppError {
  constructor(message = 'Not found') {
    super(message, 404, 'NOT_FOUND');
  }
}

export class BadRequestError extends AppError {
  constructor(message = 'Bad request') {
    super(message, 400, 'BAD_REQUEST');
  }
}

export class UnauthorizedError extends AppError {
  constructor(message = 'Unauthorized') {
    super(message, 401, 'UNAUTHORIZED');
  }
}

export class ConflictError extends AppError {
  constructor(message = 'Conflict') {
    super(message, 409, 'CONFLICT');
  }
}

export class ChatbotNotEnabledError extends AppError {
  constructor() {
    super('Chatbot is not enabled', 503, 'CHATBOT_NOT_ENABLED');
  }
}

export class LLMProviderError extends AppError {
  constructor(message = 'LLM provider error') {
    super(message, 502, 'LLM_PROVIDER_ERROR');
  }
}
